let isValid = true;
console.log(isValid, typeof(isValid));

// number
isValid = Number(isValid);
console.log("converted to number");
console.log(isValid, typeof(isValid));

// string
isValid = String(isValid);
console.log("converted to string");
console.log(isValid, typeof(isValid));

// boolean
isValid = Boolean(isValid);
console.log("converted to boolean");
console.log(isValid, typeof(isValid));

